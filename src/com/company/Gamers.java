package com.company;

public class Gamers {
    private String name;
    private int Health;
    private int Damage;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }



    public int getHealth() {
        return Health;
    }

    public void setHealth(int health) {
        Health = health;
    }



    public int getDamage() {
        return Damage;
    }

    public void setDamage(int damage) {
        Damage = damage;
    }
}
